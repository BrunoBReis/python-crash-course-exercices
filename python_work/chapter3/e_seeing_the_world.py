# Organizing list 

places = ['Japan', 'EUA', 'Australia', 'Portugal', 'Spain']

# Creating a lowercase list for places
low_places = []
for place in places:
    low_places.append(place.lower())

# Orignal list
print(low_places)

# Showing sorted list
print(sorted(low_places))

# Original list
print(low_places)

# Reversed list
print(sorted(low_places, reverse=True))

# Original list
print(low_places)

# Reversed order
low_places.reverse()
print(low_places)

# Reversed order
low_places.reverse()
print(low_places)

# Sorting list
low_places.sort()
print(low_places)

# Reverse sort
low_places.sort(reverse=True)
print(low_places)